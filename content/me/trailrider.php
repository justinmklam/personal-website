            <h1 class="page-header">MECH 328 Trailrider
            </h1>
            
            <p><strong>Background</strong>: The TrailRider™ is a specialized device to provide accessibility to the wilderness for those with limited mobility.  It is intended for a seated rider propelled and balanced by assistants.</p>
            <p><strong>Objective</strong>: Expand the range of both riders and assistants who can ride safely use the Black Diamond TrailRider™.</p>
            <p><strong>How</strong>: As a team of seven mechanical engineering undergrads, we explored a range of areas for improvement and developed a new design to incorporate these features.</p>
            <p><strong>Known Problems</strong>:</p>

            <ul style="text-align: left;">
                <li>Device relies entirely on assistants for balance, which may be physically demanding for some assistants</li>
                <li>Riders generally find the current device too passive</li>
                <li>Single-wheel design provides difficulty in rolling over abrupt trail features</li>
                <li>Single-wheel provides limited cushioning over rough terrain</li>
                <li>Device is difficult to transport and store when space is limited</li>
            </ul>
            <p><strong>Our Improvements</strong>:</p>

            <ul>
                <li style="text-align: left;">Incorporation of a 255 W electric power assist feature</li>
                <li style="text-align: left;">Implementation of lever-propulsion handles for rider engagement</li>
                <li style="text-align: left;">Integrated frame and suspension design for increased shock absorption</li>
                <li style="text-align: left;">Increased portability</li>
            </ul>

            <p><strong>Client</strong>: British Columbia Mobility Opportunities Society (BCMOS)</p>

